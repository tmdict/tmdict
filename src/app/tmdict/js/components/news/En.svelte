  <h5>2022-02-23</h5>

  <p>TMdict’s data and source code is now <a href="https://github.com/slsdo/tmdict">open to public on GitHub</a>. Feel free to take a look around, <a href="https://github.com/slsdo/tmdict/tree/main/data">update translations</a> (requires a GitHub account), contribute, or download a copy of the site for offline access!</p>

  <p><span class="highlight">Changelog:</span></p>

  <ul>
    <li>Opened up tmdict GitHub repository.</li>
  </ul>

  <h5>2021-10-24</h5>

  <p><em>Tsukihime Remake</em> was released this summer, the limited edition of the game came with a material book, the book’s glossary has now been <a href="https://www.tmdict.com/book/#tsukihime-material-i?lang=en">added to our database</a>.</p>

  <p><span class="highlight">Changelog:</span></p>

  <ul>
    <li>Added Tsukihime Material I -material of blue glass moon-: Tsukihime Work Glossary.</li>
  </ul>
  
  <h5>2020-11-26</h5>

  <p>Happy holidays! With this update comes the addition of a new <em><a href="https://www.tmdict.com/book/#prisma-material-petit?lang=en">PRISMA material/petit</a></em> glossary and the completion of <a href="https://www.tmdict.com/book/#hibichika-complete-capture-book?lang=en"><em>HibiChika Complete Capture Book</em>’s English glossary</a>. There are currently no other outstanding glossaries we are actively working on, so this will likely the last big update for a while (we’re always happy to accept user-contributed glossary translations, though!)</p>

  <p>For now we will focus on improving site stability and user experience, and continue to update <a href="https://chaldea.tmdict.com/">CHALDEAの記録</a> with new content.</p>

